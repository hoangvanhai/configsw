uint8_t crc8(const uint8_t *pBuff, int nSize) {
    int  i;
    uint8_t bCRC = 0;

    for(i = 0; i < nSize; i++) {
        bCRC = ByteCRC8(bCRC, pBuff[i]);
    }

    return bCRC;
}



unsigned char g_lowCrcbyte = 0xFF, g_highCrcbyte = 0xFF;

uint16_t crc16(uint8_t *str, uint32_t len)
{
    unsigned char temp;
    uint8_t lowCrcbyte = 0xFF, highCrcbyte = 0xFF;

    while(len--)
    {
        temp = *str++ ^ g_lowCrcbyte;
        g_lowCrcbyte = CRCHighTable[temp] ^ g_highCrcbyte;
        g_highCrcbyte = CRCLowTable[temp];
    }
    return (highCrcbyte << 8 | lowCrcbyte);
}
